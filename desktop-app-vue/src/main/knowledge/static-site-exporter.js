/**
 * 静态网站导出器
 * 将笔记集合导出为静态 HTML 网站
 */

const { logger } = require("../utils/logger.js");
const path = require("path");
const fs = require("fs");

class StaticSiteExporter {
  constructor(database) {
    this.database = database;
  }

  /**
   * Export notes as static HTML site
   * @param {string} outputDir - Output directory
   * @param {Object} options - Export options
   * @param {string} options.title - Site title
   * @param {string} options.folder - Filter by folder (optional)
   * @param {string} options.theme - Theme ('light' or 'dark')
   * @returns {Object} Export result
   */
  async exportToStaticSite(outputDir, options = {}) {
    const {
      title = "My Knowledge Base",
      folder = null,
      theme = "light",
    } = options;
    const result = { exported: 0, errors: [], outputDir };

    // Fetch notes from database
    const notes = this._fetchNotes(folder);

    if (notes.length === 0) {
      logger.warn("[StaticSiteExporter] No notes to export");
      return result;
    }

    // Create output directory
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
    }

    // Generate index page
    const indexHtml = this._generateIndexPage(title, notes, theme);
    fs.writeFileSync(path.join(outputDir, "index.html"), indexHtml, "utf8");

    // Generate individual note pages
    for (const note of notes) {
      try {
        const noteHtml = this._generateNotePage(title, note, theme);
        const slug = this._slugify(note.title || note.id);
        fs.writeFileSync(
          path.join(outputDir, `${slug}.html`),
          noteHtml,
          "utf8",
        );
        result.exported++;
      } catch (error) {
        result.errors.push({ noteId: note.id, error: error.message });
      }
    }

    // Generate CSS
    const css = this._generateCSS(theme);
    fs.writeFileSync(path.join(outputDir, "style.css"), css, "utf8");

    logger.info(
      `[StaticSiteExporter] Export complete: ${result.exported} notes exported to ${outputDir}`,
    );
    return result;
  }

  _fetchNotes(folder) {
    const db = this.database.db;
    let query = "SELECT * FROM notes ORDER BY updated_at DESC";
    const params = [];

    if (folder) {
      query = "SELECT * FROM notes WHERE folder = ? ORDER BY updated_at DESC";
      params.push(folder);
    }

    return db.prepare(query).all(...params);
  }

  _generateIndexPage(siteTitle, notes, theme) {
    const noteLinks = notes
      .map((note) => {
        const slug = this._slugify(note.title || note.id);
        const date = new Date(
          note.updated_at || note.created_at,
        ).toLocaleDateString();
        const tags = note.tags ? JSON.parse(note.tags) : [];
        const tagHtml = tags
          .map((t) => `<span class="tag">${this._escapeHtml(t)}</span>`)
          .join(" ");
        return `<li><a href="${slug}.html">${this._escapeHtml(note.title)}</a><span class="date">${date}</span>${tagHtml}</li>`;
      })
      .join("\n");

    return `<!DOCTYPE html>
<html lang="zh-CN" data-theme="${theme}">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${this._escapeHtml(siteTitle)}</title>
  <link rel="stylesheet" href="style.css">
</head>
<body>
  <header><h1>${this._escapeHtml(siteTitle)}</h1><p>${notes.length} notes</p></header>
  <main><ul class="note-list">${noteLinks}</ul></main>
  <footer><p>Generated by ChainlessChain</p></footer>
</body>
</html>`;
  }

  _generateNotePage(siteTitle, note, theme) {
    const contentHtml = this._markdownToHtml(note.content || "");
    const date = new Date(
      note.updated_at || note.created_at,
    ).toLocaleDateString();
    const tags = note.tags ? JSON.parse(note.tags) : [];
    const tagHtml = tags
      .map((t) => `<span class="tag">${this._escapeHtml(t)}</span>`)
      .join(" ");

    return `<!DOCTYPE html>
<html lang="zh-CN" data-theme="${theme}">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${this._escapeHtml(note.title)} - ${this._escapeHtml(siteTitle)}</title>
  <link rel="stylesheet" href="style.css">
</head>
<body>
  <header><a href="index.html">&larr; Back</a><h1>${this._escapeHtml(note.title)}</h1><p class="meta">${date} ${tagHtml}</p></header>
  <main class="note-content">${contentHtml}</main>
  <footer><p>Generated by ChainlessChain</p></footer>
</body>
</html>`;
  }

  _markdownToHtml(markdown) {
    if (!markdown) {
      return "";
    }
    let html = this._escapeHtml(markdown);

    // Headers
    html = html.replace(/^### (.+)$/gm, "<h3>$1</h3>");
    html = html.replace(/^## (.+)$/gm, "<h2>$1</h2>");
    html = html.replace(/^# (.+)$/gm, "<h1>$1</h1>");

    // Bold and italic
    html = html.replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>");
    html = html.replace(/\*(.+?)\*/g, "<em>$1</em>");

    // Code blocks
    html = html.replace(
      /```(\w*)\n([\s\S]*?)```/g,
      '<pre><code class="language-$1">$2</code></pre>',
    );
    html = html.replace(/`([^`]+)`/g, "<code>$1</code>");

    // Lists
    html = html.replace(/^- (.+)$/gm, "<li>$1</li>");
    html = html.replace(/(<li>.*<\/li>\n?)+/g, "<ul>$&</ul>");

    // Paragraphs
    html = html.replace(/\n\n/g, "</p><p>");
    html = "<p>" + html + "</p>";

    // Line breaks
    html = html.replace(/\n/g, "<br>");

    return html;
  }

  _generateCSS(theme) {
    const isDark = theme === "dark";
    const bg = isDark ? "#1a1a2e" : "#ffffff";
    const text = isDark ? "#e0e0e0" : "#333333";
    const accent = "#4a90d9";
    const cardBg = isDark ? "#16213e" : "#f8f9fa";

    return `:root { --bg: ${bg}; --text: ${text}; --accent: ${accent}; --card-bg: ${cardBg}; }
* { margin: 0; padding: 0; box-sizing: border-box; }
body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif; background: var(--bg); color: var(--text); max-width: 800px; margin: 0 auto; padding: 2rem; line-height: 1.6; }
header { margin-bottom: 2rem; border-bottom: 1px solid var(--accent); padding-bottom: 1rem; }
header h1 { font-size: 1.8rem; }
header a { color: var(--accent); text-decoration: none; }
.note-list { list-style: none; }
.note-list li { padding: 0.8rem; margin: 0.5rem 0; background: var(--card-bg); border-radius: 8px; display: flex; align-items: center; gap: 1rem; flex-wrap: wrap; }
.note-list li a { color: var(--accent); text-decoration: none; font-weight: 500; }
.date { color: #888; font-size: 0.85rem; }
.tag { background: var(--accent); color: white; padding: 2px 8px; border-radius: 12px; font-size: 0.75rem; }
.meta { color: #888; margin-top: 0.5rem; }
.note-content { line-height: 1.8; }
.note-content h1, .note-content h2, .note-content h3 { margin: 1.5rem 0 0.5rem; }
.note-content pre { background: var(--card-bg); padding: 1rem; border-radius: 8px; overflow-x: auto; margin: 1rem 0; }
.note-content code { background: var(--card-bg); padding: 2px 6px; border-radius: 4px; font-size: 0.9em; }
.note-content ul { margin: 0.5rem 0; padding-left: 1.5rem; }
footer { margin-top: 3rem; text-align: center; color: #888; font-size: 0.85rem; border-top: 1px solid #eee; padding-top: 1rem; }`;
  }

  _slugify(text) {
    return (
      text
        .toLowerCase()
        .replace(/[^\w\u4e00-\u9fff]+/g, "-")
        .replace(/^-+|-+$/g, "")
        .substring(0, 80) || "untitled"
    );
  }

  _escapeHtml(text) {
    if (!text) {
      return "";
    }
    return text
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;");
  }
}

module.exports = { StaticSiteExporter };
