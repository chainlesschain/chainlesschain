/**
 * Vendor the shared MCP elicitation schema into the VS Code extension.
 *
 * VSCE packages only packages/vscode-extension, so a runtime reference to the
 * sibling packages/elicitation-schema directory works in the monorepo but is
 * absent after installing the VSIX. Keep an exact, tracked copy under src/
 * and verify it before every package build.
 *
 * Usage:
 *   node scripts/sync-elicitation-schema.mjs
 *   node scripts/sync-elicitation-schema.mjs --check
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const extensionRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const sourceRoot = join(extensionRoot, "..", "elicitation-schema");
const sourcePath = join(sourceRoot, "index.js");
const sourcePackagePath = join(sourceRoot, "package.json");
const targetRoot = join(extensionRoot, "src", "vendor", "elicitation-schema");
const targetPath = join(targetRoot, "index.js");
const metadataPath = join(targetRoot, "VENDORED.md");

if (!existsSync(sourcePath) || !existsSync(sourcePackagePath)) {
  console.error(
    "packages/elicitation-schema is missing next to packages/vscode-extension",
  );
  process.exit(1);
}

const source = readFileSync(sourcePath);
const sourcePackage = JSON.parse(readFileSync(sourcePackagePath, "utf8"));
const metadata = Buffer.from(
  `# Vendored ${sourcePackage.name} v${sourcePackage.version}\n\n` +
    "Generated by scripts/sync-elicitation-schema.mjs — DO NOT EDIT.\n" +
    "Source: packages/elicitation-schema/index.js. Edit there and re-sync.\n",
  "utf8",
);

if (process.argv.includes("--check")) {
  const checks = [
    [targetPath, source],
    [metadataPath, metadata],
  ];
  for (const [file, expected] of checks) {
    if (!existsSync(file) || !readFileSync(file).equals(expected)) {
      console.error(
        `Vendored elicitation schema is stale or missing: ${file}\n` +
          "Run `node scripts/sync-elicitation-schema.mjs` and commit the result.",
      );
      process.exit(1);
    }
  }
  console.log(
    `vendored ${sourcePackage.name} v${sourcePackage.version} is current`,
  );
  process.exit(0);
}

mkdirSync(targetRoot, { recursive: true });
writeFileSync(targetPath, source);
writeFileSync(metadataPath, metadata);
console.log(
  `vendored ${sourcePackage.name} v${sourcePackage.version} -> ${targetRoot}`,
);
